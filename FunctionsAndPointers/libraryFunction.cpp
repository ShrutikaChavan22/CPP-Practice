#include<iostream>
#include<cmath>
using namespace std;
int main(){
    cout<<"Square root of 16:"<<sqrt(16)<<endl;
    cout<<"Cube root of 1000:"<<cbrt(1000)<<endl;
    cout<<"Minimum from 1,6:"<<min(1,6)<<endl;
    cout<<"Maximum from 10,100:"<<max(10,100)<<endl;
    cout<<"Power of 2,3:"<<pow(2,3)<<endl;
    return 0;
}